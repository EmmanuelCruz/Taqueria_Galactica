    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        jLabel15.setText("");
        jLabel16.setText("");
        jLabel17.setText("");
        int pos=listaOrdenDisponible.getSelectedIndex();
        if(pos<0){
            jLabel15.setText("No has seleccionado un taco galáctico");
        }
        int ingre=jList2.getSelectedIndex();
        if(ingre<0){
            jLabel16.setText("No has seleccionado un ingrediente");
        }
        if(pos<0 || ingre<0)
            return;
        Taco t=sucursal.orden.get(pos);
        switch(ingre){
            case 0:
                t=new SalsaGalactica(t);
                break;
            case 1:
                t=new CarneMarciana(t);
                break;
            case 2:
                t=new VerduAsteroide(t);
                break;
            case 3:
                t=new AderezoCometa(t);
                break;
            case 4:
                t=new OjoMarciano(t);
                break;
            case 5:
                t=new ExplosionMeteorito(t);
                break;
            case 6:
                t=new ComplementoEstrella(t);
                break;
            case 7:
                t=new PolvoViaLactea(t);
                break;
        }
        sucursal.orden.remove(pos);
        sucursal.orden.add(pos,t);
        actualizaOrden();
        sucursal.ganancias=sucursal.totalPago();
        jLabel15.setText("Ingrediente agregado a tu taco");
        jLabel16.setText(t.obtenNombre());
        jLabel18.setText("Total a pagar: "+sucursal.ganancias);
    }//GEN-LAST:event_jButton9ActionPerformed

