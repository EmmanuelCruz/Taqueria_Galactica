    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        jLabel35.setText("");
        jLabel36.setText("");
        int id=(Integer) jSpinner2.getValue();
        boolean busca=sucursal.buscar(id);
        if(!busca){
            jLabel35.setText("El id "+id+" no existe :(");
            return;
        }
        String comentario=jTextField2.getText();
        if(comentario.length()<=0){
            jLabel35.setText("No ingresaste un comentario");
            return;
        }
        sucursal.registraComentario(comentario);
        Iterator it=sucursal.opiniones.iterator();
        coments.clear();
        while(it.hasNext()){
            coments.addElement((String) it.next());
        }
        jTextField2.setText("");
        jLabel35.setText("Gracias por tu opinión");
        jLabel36.setText(sucursal.obtenNombreCliente(id));
    }//GEN-LAST:event_jButton12ActionPerformed

