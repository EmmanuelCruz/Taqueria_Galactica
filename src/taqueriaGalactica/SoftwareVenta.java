    /**
     * Actualiza la tabla de orden.
     */
    private void actualizaOrden(){
        Iterator it=sucursal.orden.iterator();
        Taco taco=null;
        listaOrden.clear();
        while(it.hasNext()){
            taco=(Taco) it.next();
            listaOrden.addElement(taco);
        }
    }
    
